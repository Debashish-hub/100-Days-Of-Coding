int diff(Node *root, int &res){
    if(!root){
        return INT_MAX;
    }
    if(!root->left && !root->right){
        return root->data;
    }
    int l = diff(root->left, res);
    int r = diff(root->right, res);
    int m = min(l,r);
    res = max(res, root->data - m);
    
    return min(m, root->data);
    
}
//Function to return the maximum difference between any node and its ancestor.
int maxDiff(Node* root)
{
    // Your code here 
    int res = INT_MIN;
    diff(root, res);
    return res;
}
