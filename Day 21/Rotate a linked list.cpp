class Solution
{
    public:
    //Function to rotate a linked list.
    Node* rotate(Node* head, int k)
    {
        // Your code here
        Node * curr = head;
        Node *end = head;
        int count = 0;
        while(curr->next != NULL){
            curr = curr->next;
        }
        curr->next = head;
        while(count != k){
            count++;
            end = head;
            head = head->next;
        }
        end->next = NULL;
        return head;
    }
};
    


